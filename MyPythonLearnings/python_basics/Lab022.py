# Not operator is used with the boolean values
x = 10
y = 60
result = (x != y)
print(result)

# Assignment operator(=) : it assigns the value and doesn't return
q = 10
r = 30    # r is assigned to value 30

""" Comparison operator(==) : it checks whether both variables are equal or not 
 and returns boolean values  """
x = 5
y = 5
result = (x == y)
print(result)

"""  Exponential operator :it is used to raise the number 
on the left to the power of the exponent of the right  """
c = 2 ** 3
print(c)

# Bitwise XOR :it is used to compare binary numbers
a = 60
b = 13
c = a ^ b
print(c)

