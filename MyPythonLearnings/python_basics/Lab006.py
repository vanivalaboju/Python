#  Datatype - what type of data we can store in a variable
#  Integer means real or whole numbers without decimal it contains +ve & -ve numbers
x = 10  # x is an integer data type
print(x)
print(type(x))
# Float means decimal numbers with +ve & -ve decimals
pi = 3.14  # pi is a floating point data type
print(pi)
print(type(pi))
# string means sequence of character with single and double quotes
name = "Tapasya"  # name is a string data type
name = 'Athulith'
print(name)
print(type(name))
# Boolean - it can take True or False values
isMarried = True  # isMarried ia a boolean data type
print(isMarried)
print(type(isMarried))
# complex means imaginary numbers, it is used AI,ML,DS,Math they needed
a = 1 + 7j  # a is a complex data type
print(a)
print(type(a))


