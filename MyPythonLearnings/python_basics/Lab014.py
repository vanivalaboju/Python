""" Escape sequences - they are special combinations of characters that have a meaning
 other than the literal characters
 \t - tabspace
 \b - Backspace
 \n - Newline
 \r - carriage return
 \f - formfeed
 \\ - Backslash character """
print("a\nb")
print("a\tb")
print("a\rb")
print("a\fb")
print(r"a\\b")