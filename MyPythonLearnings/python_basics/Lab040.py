""" Function: it is a reusable set of operations
    functions are defined using def keyword
   followed by function name and paranthesis ()
   Block of code which will help you to execute some task"""
# 1. Built in functions
# len(),min(),max() and print() are examples of built in functions
result = max(9, 6)
print(result)
r = min(6, 3)
print(r)

# 2 Non Return Type of F(n)-
# Define F(n)
def greeting():
    print("Happy New Year!")
    print("Have a wonderful year ahead!")
# Call a F(n)
result = greeting()
print(result)

